        private void tblEquiposMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEquiposMouseClicked
		// TODO add your handling code here:
		//OBTENER EL VALOR DE LA CELDA "ID" DE LA FILA SELLECCIONADA DE LA TABLA EQUIPOS
		int id = Integer.parseInt(String.valueOf(tblEquipos.getValueAt(tblEquipos.getSelectedRow(), 0)));
		//OBTENER UN EQUIPO EN BASE A NUESTRO ID
		equipo = FacadeEquipos.getEquipo(id);
        }//GEN-LAST:event_tblEquiposMouseClicked

        private void btnAceptarEquipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarEquipoActionPerformed
		// TODO add your handling code here:
		//ENVIAR EL EQUIPO A LA CLASE ACEPTAR DATOS PARA QUE SEA USADO POR LA CLASE PRESTAMOS ITCA
		//HACER REFERENCIA A LA CLASE ACEPTAR DATOS
		AceptarDatos aceptar = new AceptarDatos(equipo);
		aceptar.registrarEquipo();
		parentFrame.txtEquipo.setText(equipo.getDescripcion());
		//CERRAR ESTA VENTANA
		this.dispose();
        }//GEN-LAST:event_btnAceptarEquipoActionPerformed

